def format_today_cn(d: date) -> str:
    return f"今日{d.year}年{d.month}月{d.day}日，{WEEKDAY_CN[d.weekday()]}"


def solar_term_for_date(d: date) -> str:
    terms = SOLAR_TERMS_BY_YEAR.get(d.year, [])
    for name, month, day in terms:
        if d.month == month and d.day == day:
            return name
    return "平常日"


def display_version(raw: str) -> str:
    return raw.lstrip("vV").strip()
